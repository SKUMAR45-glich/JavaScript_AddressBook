class ContactDetails {
    firstName;
    lastName;
    address;
    city;
    state;
    zip;
    phNumber;
    email;

    ContactDetails(firstName, lastName, address, city, state, zip, phNumber, email) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.address = address;
        this.city = city;
        this.state = state;
        this.zip = zip;
        this.phNumber = phNumber;
        this.email = email;
    }
}